import mongoose from "mongoose";

// This file defines table in database
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  }
})

//users table
export default mongoose.model("user", userSchema);